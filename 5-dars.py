#                           TAQQOSLASH OPERATORLARI

#Operator-----------------------------------------Ma'nosi-----------------------------------------Namuna
#   >             agar chapdagi operand o'ngdagisidan katta bo'lsa True qaytaradi                 x > y
#   <             agar chapdagi operand o'ngdagisidan kichik bo'lsa True qaytaradi                x < y
#  ==             agar ikkita operand teng bo'lsa True qaytaradi                                  x == y
#  !=             agar ikkita opernad teng bo'lmasa Ture qaytaradi                                x != y
#  >=             katta yoki teng                                                                 x >= y
#  <=             kichik yoki teng  
# 


#Namuna uchun misollar

x = 10
y = 5

print("x>y: ", x>y)
print("x<y: ", x<y)
print("x==y: ", x==y)
print("x!=y: ", x!=y)
print("x>=y: ", x>=y)
print("x>=y: ", x<=y)
